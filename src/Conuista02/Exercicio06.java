package Conuista02;

public class Exercicio06{
    public static void main(String[] args) {


        String car = "Pc Gamer";

        switch (car) {

            case "Xbox":
                System.out.println("Xbox");
                break;

            case "PS5":
                System.out.println("PS5");
                break;

            case "Pc":
                System.out.println("Pc");
                break;

            case "switch":
                System.out.println("switch");
                break;


            default:
                System.out.println("O valor não é compatível");
        }

    }
}
